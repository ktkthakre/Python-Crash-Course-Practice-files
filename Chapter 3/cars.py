#sorting a list with sort() method
cars = ['bmw','audi','aston martin','jaguar']
print(cars)
cars.sort()
print(cars)

#sorting temperorily
cars = ['bmw','audi','aston martin','jaguar']
print(sorted(cars))

#reversing
cars = ['bmw','audi','aston martin','jaguar']
print(cars)
cars.reverse()
print(cars)

#finding length of string
cars = ['bmw','audi','aston martin','jaguar']
print(len(cars))

